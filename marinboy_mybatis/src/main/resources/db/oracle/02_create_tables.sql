CREATE TABLE MB_USER (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(60 CHAR) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(120 CHAR) NOT NULL,
    NAME VARCHAR2(80 CHAR) NOT NULL,
    EMAIL VARCHAR2(120 CHAR) NOT NULL,
    PHONE VARCHAR2(30 CHAR) NOT NULL,
    ROLE VARCHAR2(20 CHAR) NOT NULL,
    SOCIAL_PROVIDER VARCHAR2(30 CHAR),
    SOCIAL_ID VARCHAR2(160 CHAR),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_MB_USER_ROLE CHECK (ROLE IN ('CUSTOMER', 'ADMIN'))
);

-- 고객이 예약할 수 있는 시술/뷰티 메뉴 기본 정보를 저장하는 테이블입니다.
CREATE TABLE MB_SERVICE_ITEM (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100 CHAR) NOT NULL,
    CATEGORY VARCHAR2(30 CHAR) NOT NULL,
    DURATION_MINUTES NUMBER NOT NULL,
    PRICE NUMBER(10, 2) NOT NULL,
    DESCRIPTION VARCHAR2(255 CHAR) NOT NULL,
    TOP_RANK NUMBER,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_MB_SERVICE_DURATION_UNIT CHECK (DURATION_MINUTES >= 10 AND MOD(DURATION_MINUTES, 10) = 0),
    CONSTRAINT CK_MB_SERVICE_PRICE_UNIT CHECK (PRICE >= 1000 AND MOD(PRICE, 1000) = 0),
    CONSTRAINT CK_MB_SERVICE_TOP_RANK CHECK (TOP_RANK IS NULL OR TOP_RANK BETWEEN 1 AND 3)
);

-- 시술 메뉴의 대표 이미지와 추가 갤러리 이미지를 저장하는 테이블입니다.
-- 대표 이미지는 REPRESENTATIVE, 추가 이미지는 DETAIL 타입으로 구분합니다.
CREATE TABLE MB_SERVICE_IMAGE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SERVICE_ID NUMBER NOT NULL,
    IMAGE_URL VARCHAR2(500 CHAR) NOT NULL,
    IMAGE_TYPE VARCHAR2(20 CHAR) NOT NULL,
    SORT_ORDER NUMBER DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_MB_SERVICE_IMAGE_SERVICE
        FOREIGN KEY (SERVICE_ID) REFERENCES MB_SERVICE_ITEM (ID) ON DELETE CASCADE,
    CONSTRAINT CK_MB_SERVICE_IMAGE_TYPE CHECK (IMAGE_TYPE IN ('REPRESENTATIVE', 'DETAIL'))
);

-- 고객 예약과 관리자 예약 상태 변경에 사용하는 예약 테이블입니다.
-- 고객명/연락처/이메일은 예약 당시 값을 보존하기 위해 스냅샷으로 함께 저장합니다.
CREATE TABLE MB_RESERVATION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SERVICE_ID NUMBER NOT NULL,
    CUSTOMER_ID NUMBER NOT NULL,
    CUSTOMER_NAME VARCHAR2(80 CHAR) NOT NULL,
    CUSTOMER_EMAIL VARCHAR2(120 CHAR) NOT NULL,
    CUSTOMER_PHONE VARCHAR2(30 CHAR) NOT NULL,
    RESERVATION_DATE_TIME TIMESTAMP NOT NULL,
    NO_SHOW_POLICY_AGREED NUMBER(1) DEFAULT 1 NOT NULL,
    STATUS VARCHAR2(20 CHAR) NOT NULL,
    MEMO VARCHAR2(500 CHAR),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_MB_RESERVATION_SERVICE
        FOREIGN KEY (SERVICE_ID) REFERENCES MB_SERVICE_ITEM (ID),
    CONSTRAINT FK_MB_RESERVATION_CUSTOMER
        FOREIGN KEY (CUSTOMER_ID) REFERENCES MB_USER (ID),
    CONSTRAINT CK_MB_RESERVATION_STATUS
        CHECK (STATUS IN ('REQUESTED', 'CONFIRMED', 'REJECTED', 'CANCELED', 'COMPLETED', 'NO_SHOW')),
    CONSTRAINT CK_MB_RESERVATION_POLICY CHECK (NO_SHOW_POLICY_AGREED IN (0, 1))
);

-- 관리자가 지정한 특정 휴무일을 저장하는 테이블입니다.
CREATE TABLE MB_HOLIDAY (
    HOLIDAY_DATE DATE PRIMARY KEY,
    REASON VARCHAR2(200 CHAR)
);
