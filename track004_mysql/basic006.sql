-- -- emp테이블에서  
-- -- 추가수당(comm) 이 없고 
-- -- 상급자(mgr)은 존재하며
-- -- 직책(JOB) 'MANAGER', 'CLERK' 중에서 
-- -- 사원이름의 두번째 글자가 L이아닌
-- -- 사원데이터를  조회하시오
